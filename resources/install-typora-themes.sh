# Minimalism
    # Download
    wget -O minimalism.zip https://github.com/pink-bird/typora-theme-gallery/blob/gh-pages/media/theme/minimalism/minimalism.zip?raw=true
    # Unzip
    unzip minimalism.zip
    # Install
    mv -r minimalism ~/.config/Typora/themes/minimalism
    mv minimalism.css ~/.config/Typora/themes/minimalism.css
    mv minimalism-dark.css ~/.config/Typora/themes/minimalism-dark.css
    # Cleanup
    rm -r minimalism.zip __MACOSX minimalism

# Ash
    # Download
    wget https://github.com/typora/typora-ash-theme/archive/master.zip
    # Unzip
    unzip master.zip
    # Install
    cp -r typora-ash-theme-master/ash ~/.config/Typora/themes/ash
    cp typora-ash-theme-master/ash.css ~/.config/Typora/themes/ash.css
    # Cleanup
    rm -r master.zip typora-ash-theme-master

